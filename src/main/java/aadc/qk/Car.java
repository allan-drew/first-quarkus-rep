package aadc.qk;

public class Car {

    public int id;
    public String brand;
    public String name;
    public int year;
    public String condition;

    public Car(int id, String brand, String name, int year, String condition) {
        this.id = id;
        this.brand = brand;
        this.name = name;
        this.year = year;
        this.condition = condition;
    }


}
